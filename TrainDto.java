package master.dto;

public class TrainDto {
private int sno;
private String tnum;
private String tname;
private String sstn;
private String estn;

public int getSno() {
	return sno;
}
public void setSno(int sno) {
	this.sno=sno;
}
public String getTnum() {
	return tnum;
}
public void setTnum(String tnum) {
	this.tnum = tnum;
}
public String getTname() {
	return tname;
}
public void setTname(String tname) {
	this.tname = tname;
}
public String getSstn() {
	return sstn;
}
public void setSstn(String sstn) {
	this.sstn = sstn;
}
public String getEstn() {
	return estn;
}
public void setEstn(String estn) {
	this.estn = estn;
}

}
